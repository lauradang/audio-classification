import matplotlib.pyplot as plt
import pandas as pd 
from scipy.io import wavfile
import os

def df_insert_length(csv_file, directory):
    df = pd.read_csv(csv_file)
    df.set_index("fname", inplace=True)
    for fname in df.index:
        sample_rate, samples = wavfile.read(os.path.join(directory, fname))
        df.at[fname, "length"] = samples.shape[0] / sample_rate
    return df

def plot_category_distribution(categories_dist):
    fig, ax = plt.subplots()
    ax.set_title("Class Distribution")
    ax.pie(categories_dist, labels=categories_dist.index, autopct="%1.1f%%")
    
def plot_signals(signals):
    fig, axes = plt.subplots(nrows=2, ncols=5, sharex=False,
                             sharey=True, figsize=(20,5))
    fig.suptitle("Time Series", size=16)
    i = 0
    for x in range(2):
        for y in range(5):
            axes[x,y].set_title(list(signals.keys())[i])
            axes[x,y].plot(list(signals.values())[i])
            axes[x,y].get_xaxis().set_visible(False)
            axes[x,y].get_yaxis().set_visible(False)
            i += 1

def plot_fft(fft):
    fig, axes = plt.subplots(nrows=2, ncols=5, sharex=False,
                             sharey=True, figsize=(20,5))
    fig.suptitle("Fourier Transforms", size=16)
    i = 0
    for x in range(2):
        for y in range(5):
            data = list(fft.values())[i]
            Y, freq = data[0], data[1]
            axes[x,y].set_title(list(fft.keys())[i])
            axes[x,y].plot(freq, Y)
            axes[x,y].get_xaxis().set_visible(False)
            axes[x,y].get_yaxis().set_visible(False)
            i += 1

